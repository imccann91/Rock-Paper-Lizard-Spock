package edu.clarkson.mccannim.RPSLSGame;

/**
 * Class that describes the Spock weapon.
 *
 *Extends Weapon.
 */
public class Spock extends Weapon {
	/**
	 * Returns the string "Spock." so that a sentence can be constructed from
	 * the weapon and its modifier.
	 * 
	 * @param There are no input arguments.
	 * 
	 * @return String "Spock."
	 */
	@Override
	public String getPhrase() {
		return "Spock.";
	}

}
